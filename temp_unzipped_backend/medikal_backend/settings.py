# Database
# Connect to PostgreSQL, fallback gracefully to SQLite if connection fails or parameters are missing
database_url = os.getenv('DATABASE_URL')
if database_url:
    import urllib.parse
    try:
        urllib.parse.uses_netloc.append("postgres")
        url = urllib.parse.urlparse(database_url)
        DB_NAME = url.path[1:]
        DB_USER = url.username
        DB_PASSWORD = url.password
        DB_HOST = url.hostname
        DB_PORT = url.port
    except Exception as e:
        print(f"WARNING: Failed to parse DATABASE_URL ({e})")
        DB_NAME = os.getenv('DB_NAME')
        DB_USER = os.getenv('DB_USER')
        DB_PASSWORD = os.getenv('DB_PASSWORD')
        DB_HOST = os.getenv('DB_HOST')
        DB_PORT = os.getenv('DB_PORT')
else:
    DB_NAME = os.getenv('DB_NAME')
    DB_USER = os.getenv('DB_USER')
    DB_PASSWORD = os.getenv('DB_PASSWORD')
    DB_HOST = os.getenv('DB_HOST')
    DB_PORT = os.getenv('DB_PORT')

DATABASES = {
    'default': {
        'ENGINE': 'django.db.backends.sqlite3',
        'NAME': BASE_DIR / 'db.sqlite3',
    }
}

if all([DB_NAME, DB_USER, DB_PASSWORD, DB_HOST, DB_PORT]):
    try:
        import psycopg2
        # Test connection briefly before setting it as default database
        conn = psycopg2.connect(
            dbname=DB_NAME,
            user=DB_USER,
            password=DB_PASSWORD,
            host=DB_HOST,
            port=DB_PORT,
            connect_timeout=2
        )
        conn.close()
        DATABASES['default'] = {
            'ENGINE': 'django.db.backends.postgresql',
            'NAME': DB_NAME,
            'USER': DB_USER,
            'PASSWORD': DB_PASSWORD,
            'HOST': DB_HOST,
            'PORT': DB_PORT,
        }
        print("Successfully connected to PostgreSQL database.")
    except Exception as e:
        print(f"WARNING: Could not connect to PostgreSQL ({e}). Falling back to local SQLite database.")
else:
    print("WARNING: PostgreSQL environment variables are incomplete. Falling back to local SQLite database.")

